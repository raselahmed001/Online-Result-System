        function typing_effect(words, colors) {

            var cursor = document.getElementById('cursor'); 
            var text = document.getElementById('text') 

            var blink = true;
            var wait = false;
            var letters_count = 1;
            var temp = 1;

            text.style.color = colors[0]
            window.setInterval(function() { 
                if (letters_count === 0 && wait === false) {
                    wait = true;

                    text.innerHTML = '' 
                    window.setTimeout(function() {
                        var usedColor = colors.splice(0, 1)[0] 
                        colors.push(usedColor);
                        var usedWord = words.splice(0, 1)[0]
                        words.push(usedWord);
                        temp = 1;
                        text.style.color = colors[0]
                        letters_count += temp;
                        wait = false;
                    }, 1000)
                } else if (letters_count === words[0].length + 1 && wait === false) {
                    wait = true;
                    window.setTimeout(function() { 
                        temp = -1;
                        letters_count += temp;
                        wait = false;
                    }, 1000)
                } else if (wait === false) {                    
                    text.innerHTML = words[0].substr(0, letters_count)
                    letters_count += temp;
                }
            }, 130)
            window.setInterval(function() {
                if (blink) {
                    cursor.style.opacity = "0";
                    blink = false;
                } else {
                    cursor.style.opacity = "1";
                    blink = true;
                }
            }, 200)
        }
